void main() {
  int noOfDay = 7;

  if (noOfDay == 1) {
    print("Day is sunday");
  } else if (noOfDay == 2) {
    print("Day is monday");
  } else if (noOfDay == 3) {
    print("Day is tuesday");
  } else if (noOfDay == 7) {
    print("Day is saturday");
  } else {
    print("invalid option given");
  }
}
